package com.dat.backend_v2_1.enums.Core;

public enum CoachStatus {
    ACTIVE, // Đang làm việc
    INACTIVE, // Tạm ngưng công việc (ví dụ: nghỉ phép, tạm thời không làm việc)
    SUSPENDED, // Bị đình chỉ công việc (ví dụ: vi phạm nội quy, đang chờ xử lý kỷ luật)
    RETIRED // Đã nghỉ hưu, không còn làm việc nữa
}
